const mongoose = require("mongoose")

const prodcutSchema = new mongoose.Schema({

    name:{
        type:String,
        required:true,
        trim:true,
    },
    price:{
        type:Number,
        required:true,
    },
    category:{
        type:String,
        required:true
    }
    

}, { timestamps: true })

module.exports = mongoose.model("product", prodcutSchema)